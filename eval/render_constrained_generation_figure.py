"""Render the REAL constrained generation results (from run_constrained_generation.py)
as a paper-style SMPL mesh figure. Green = truly constrained (kept from GT via
inpaint mask), blue = truly generated by the model."""
import argparse
import pickle
from pathlib import Path

import matplotlib.pyplot as plt
import numpy as np

import sys

sys.path.insert(0, str(Path(__file__).resolve().parent))
from render_smpl_mesh_grid import (
    CONSTRAINT_BG,
    CONSTRAINT_COLOR,
    GENERATED_BG,
    GENERATED_COLOR,
    LOWER_BODY_ROOT,
    UPPER_BODY,
    build_meshes,
    render_mesh,
)


def parse_args():
    parser = argparse.ArgumentParser()
    parser.add_argument("--in_dir", default="eval/ablation/constrained_generation")
    parser.add_argument("--smpl_model", default="smpl_model/SMPL_NEUTRAL.pkl")
    parser.add_argument("--out", default="eval/ablation/ccl_visualizations/real_constrained_generation_figure.png")
    parser.add_argument("--num_frames", type=int, default=7)
    parser.add_argument("--img_size", type=int, default=400)
    return parser.parse_args()


ROWS = [
    ("seed", "completion from seed motion"),
    ("keyframe", "specified middle keyframe"),
    ("upper", "upper-body constraints"),
    ("lower", "lower-body + root constraints"),
]


def main():
    args = parse_args()
    in_dir = Path(args.in_dir)

    fig, axes = plt.subplots(len(ROWS), args.num_frames, figsize=(args.num_frames * 1.6, len(ROWS) * 1.75), dpi=180)
    for row, (key, title) in enumerate(ROWS):
        with open(in_dir / f"constrained_{key}.pkl", "rb") as f:
            data = pickle.load(f)
        poses = data["smpl_poses"]
        trans = data["smpl_trans"]
        mask_frames = np.asarray(data["mask_frames"], dtype=bool)
        frames = np.linspace(0, len(poses) - 1, args.num_frames, dtype=int)
        vertices, faces, skin_weights = build_meshes(args.smpl_model, poses, trans, frames)

        def part_colors(joints):
            w = skin_weights[:, joints].sum(axis=1)
            m = (w > 0.5)[:, None]
            base = np.tile(np.array(GENERATED_COLOR), (skin_weights.shape[0], 1))
            green = np.tile(np.array(CONSTRAINT_COLOR), (skin_weights.shape[0], 1))
            return np.where(m, green, base)

        for col, frame in enumerate(frames):
            vertex_colors = None
            color = GENERATED_COLOR
            bg = GENERATED_BG
            if key in ("seed", "keyframe"):
                if mask_frames[frame]:
                    color = CONSTRAINT_COLOR
                    bg = CONSTRAINT_BG
            elif key == "upper":
                vertex_colors = part_colors(UPPER_BODY)
                bg = CONSTRAINT_BG
            elif key == "lower":
                vertex_colors = part_colors(LOWER_BODY_ROOT)
                bg = CONSTRAINT_BG
            rgb = render_mesh(vertices[col], faces, color, args.img_size, bg, vertex_colors=vertex_colors)
            ax = axes[row, col]
            ax.imshow(rgb)
            ax.set_axis_off()
            if col == 0:
                ax.text(-0.12, 0.5, title, transform=ax.transAxes, fontsize=7, rotation=90, va="center")
    plt.subplots_adjust(wspace=0.02, hspace=0.02)
    Path(args.out).parent.mkdir(parents=True, exist_ok=True)
    plt.savefig(args.out, bbox_inches="tight")
    print(args.out)


if __name__ == "__main__":
    main()
